package task_and_interview.intprep;

class ForLoopIQFour {
    public static void main(String[] args) {
        for(int i=0; i>10; ){
            System.out.println("Hello"); //Nothing will print
        }
        for(int i=0; i<10;){
            System.out.println(i); //Infinite Loop
        }
    }
}
