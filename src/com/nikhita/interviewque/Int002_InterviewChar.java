package com.nikhita.interviewque;

public class Int002_InterviewChar {
    public static void main(String[] args) {
        char a = 'A'; //65 ASCII Decimal
        char b = 'B'; //66 ASCII Decimal
        char c = 'a'; //97
        char d = 'b'; //98
        String e = "e";
        String f = "f";
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

        System.out.println(a+b+c+d); //char concatenation
        System.out.println(e+f); //String concatenation
    }
}
